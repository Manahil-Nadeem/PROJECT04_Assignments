def main():
    # Countdown from 10 to 1
    for i in range(10, 0, -1):  # Start at 10, stop before 0, and decrement by 1 each time
        
        print(i, end=" ")  # Print the current number 
        
    # After the countdown, print "Liftoff!"
    print("Liftoff!")

#  To run the main() function
if __name__ == '__main__':
    main()
